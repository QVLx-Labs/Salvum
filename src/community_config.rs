#[allow(dead_code)]
pub struct Entry<'a> {
  pub name: &'a str,
  pub fn_ptr: fn(Vec<String>, String, String) -> Box<dyn events::Eventable + Send + Sync>,
  pub included: bool,
  pub unsafe_dir_access: bool,
  pub nonlocal_ip_access: bool,
  pub rpt_enabled: bool,
  pub children: Vec<Entry<'a>>,
}

#[allow(dead_code)]
#[derive(Clone, Copy, Debug, PartialEq)]
pub enum EntryStatus {
  Default,
  Enabled,
  Disabled,
}

/*
 * generate
 * 
 * @brief
 */
#[allow(dead_code)]
pub fn generate() {
  // Ensure we don't accidentally overwrite any existing configs
  if std::path::Path::new("cfg/salvum.cfg").exists() {
    if !alerts::confirm_task("A config for salvum already exists, would you like to overwrite it?") {
      return;
    }
  }

  // Create a file
  let mut file = match std::fs::File::create("cfg/salvum.cfg") {
    Ok(filename) => filename,
    Err(_) => {
      return;
    }
  };

  // Create the config string
  fn traverse_entries(entry: Entry) -> String {
    let mut _str = String::new();
    if entry.children.len() > 0 {
      for child in entry.children {
        _str.push_str(&traverse_entries(child));
      }
    } else {
      _str = format!("{}:\n  included:default\n  reporting:default\n", entry.name);
    }
    return _str;
  }
  let fs_string = traverse_entries(get_entries());

  // Write to the file
  match file.write(fs_string.as_bytes()) {
    Ok(_) => {}
    Err(err) => {
      println!("Failed to write to cfg/salvum.cfg: {}", err);
    }
  };
}

/*
 * read_config
 * 
 * @brief
 * @return 
 */
#[allow(dead_code)]
pub fn read_config() -> HashMap<String, (EntryStatus, EntryStatus)> {
  // Ensure a config exists before doing anything
  if !std::path::Path::new("cfg/salvum.cfg").exists() {
    generate();
  }

  // Read the config
  let fs_str = match std::fs::read_to_string("cfg/salvum.cfg") {
    Ok(out) => out,
    Err(err) => {
      println!("Failed to read the salvum config: {}", err);
      return HashMap::new();
    }
  };
  let lines: Vec<&str> = fs_str.split("\n").collect();

  // Generate the status vector
  let mut config_statuses: HashMap<String, (EntryStatus, EntryStatus)> = HashMap::new();
  let mut current_event = String::new();
  let mut included_stat = EntryStatus::Default;
  let mut reporting_stat = EntryStatus::Default;
  for line in lines {
    let div: Vec<&str> = line.split(":").collect();

    // Update included status
    if div[0].contains("included") {
      included_stat = match div[1] {
        "yes" | "enable" | "true" => EntryStatus::Enabled,
        "no" | "disable" | "false" => EntryStatus::Disabled,
        _ => EntryStatus::Default
      };
    // Update reporting status
    } else if div[0].contains("reporting") {
      reporting_stat = match div[1] {
        "yes" | "enable" | "true" => EntryStatus::Enabled,
        "no" | "disable" | "false" => EntryStatus::Disabled,
        _ => EntryStatus::Default
      };
    // New event, add current state to the vector and restart
    } else {
      // Don't add blank events
      if current_event != "" { config_statuses.insert(current_event, (included_stat, reporting_stat)); }
      current_event = div[0].to_string();
      included_stat = EntryStatus::Default;
      reporting_stat = EntryStatus::Default;
    }
  }

  return config_statuses;
}

